// 1. booleans & comparison operator

let num1=3;
let num2=6;
console.log(num1>num2); // returns false

// 2. == vs ===

let n1="5";
let n2=5;

console.log(n1==n2); // returns true - only checks value and not data type.
console.log(n1===n2); // returns false - checks for data type also.

// 3. != vs !==
console.log(n1!=n2); // returns false - which means they are both same but is NOT!!!
console.log(n1 !== n2); // returns true
